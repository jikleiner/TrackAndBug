CREATE TABLE tb.COMENTARIOS(
    id_comentario NUMBER GENERATED BY DEFAULT AS IDENTITY,
    id_tarea NUMBER NOT NULL,
    id_usuario NUMBER NOT NULL,
    cuerpo_comentario VARCHAR2(200) NOT NULL,
    fecha DATE NOT NULL,
    PRIMARY KEY(id_comentario),
    CONSTRAINT FK_TAREA_COM FOREIGN KEY(id_tarea)
    REFERENCES TAREAS(id_tarea),
    CONSTRAINT FK_USUARIO_COM FOREIGN KEY(id_usuario)
    REFERENCES USUARIOS(id_usuario)
);
